# Define a number 10 and then use if-else statements to check whether it is
# positive, negative, or zero. The output will be "The number is positive."
# since 10 is greater than 0.

# define number variable and set it equal to 10
number = 10

# check if the  number is positive, negative,  or zero and print the result

if (number > 0):
    print("The number is positive!")
elif (number < 0):
    print("The number is negatiive!")
else:
    print("The number is 0!")
